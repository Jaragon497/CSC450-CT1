/*
 *  A simple program that prints out the name
 *  and address of a fictional character.
 */

#include <iostream>

// Main function
int main(int argc, char *argv[]) {

    // Print functions on newlines
    std::cout << "First Name:\tMax" << std::endl;
    std::cout << "Last Name:\tRockatansky" << std::endl;
    std::cout << "Address:\t2015 Fury Road" << std::endl;
    std::cout << "City:\t\tDarwin, Northern Territory [Australia]" << std::endl;
    std::cout << "Zipcode:\t90909" << std::endl;

    // Successful exit
    return 0;
}
